    public int canCompleteCircuit(int[] gas, int[] cost) {
        int length = gas.length;
        if (length == 1 && gas[0]>=cost[0]){
            return 0;
        }
        for (int i=0;i<length;i++){
            if (gas[i]>cost[i]){
                boolean isGo = false;
                int leaveGas = 0;
                for (int j=i;j<length;){
                    leaveGas += gas[j];
                    leaveGas -= cost[j];
                    if ( leaveGas < 0){
                        break;
                    }
                    if (isGo&&((i!=0&&j==i-1)||(i==0&&j==i))){
                        return i;
                    }
                    if (j==length-1){
                        j = 0;
                    }else{
                        j++;
                        isGo = true;
                    }
                }
            }
        }
        return -1;
    }

